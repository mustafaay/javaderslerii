package ders15;

public class C06_ForLoop {
    public static void main(String[] args) {
        //100 den 1 e kadar sinirlar dahil olmak uzere 8 ile bolunen sayilari yazdirin


        for (int i = 100; i > 0; i--) {

            if (i % 8 == 0) {

                System.out.println(i);


            }

        }


    }


}
