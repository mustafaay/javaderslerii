package practice1ve2;

public class Q04_javaVarible {

    public static void main(String[] args) {

// Initializing Variable  //variable baslangic degeri verme
        int sayi1 = 55;
        double sayi2 = 22.22222;


        System.out.println("sayi1 = " + (sayi1 + 45));
        System.out.println("sayi2 = " + (sayi2 + 77.77778));

// To copy the variable's value
        int l = 88;
        int j = l;
        System.out.println("j = " + j);
        System.out.println("l = " + l);

// you can declare multiple variables in the same line

        int g = 11, u = 99;
        System.out.println("g = " + g);
        System.out.println("u = " + u);


// updating a variable
        g += 44;
        u += 1;
        System.out.println("g = " + g);
        System.out.println("u = " + u);


//Bir degisken tanimlayin : x
//Baska bir degiskeni initialize edin : y
//x degiskenini initialize edin
//y degiskenini yeni bir degerle guncelleyin


        int a;
        int b = 55;

        a = 45;
        b = 75;


        System.out.println("b = " +b);
        System.out.println("a = " + a);

// kodlari duzene sokmak icin ctrl+alt+l

// eger ctrl+mouse tekeriniz ile fontu buyultup kucultebiliriz


    }

}
